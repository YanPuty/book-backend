const mongoose = require("mongoose");
const Schema = mongoose.Schema;

// Define the schema
const bookFilesSchema = new Schema(
  {
    url: {
      type: String,
      trim: true,
      required: true,
    },
    name: {
      type: String,
      trim: true,
      required: true,
    },
    extension: {
      type: String,
      trim: true,
      required: true,
    },
    type: {
      type: String,
      trim: true,
      required: true,
    },
  },
  {
    timestamps: true,
    toJSON: {
      transform: function (_doc, ret) {
        // Transform the _id field to id
        ret.id = ret._id;
        delete ret._id;
        delete ret.__v;
      },
    },
  },
);

// Define and export the Organization model
const BookFiles = mongoose.model("BookFiles", bookFilesSchema);
module.exports = BookFiles;
